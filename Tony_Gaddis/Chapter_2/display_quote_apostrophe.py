#Строки, которые заключены в ''' ''' (тройной апостроф) и """ """ (тройные кавычки)
#, внутри могут сожержать ' ' (одинарные кавычки) и " " (двойные кавычки)
print('''Вместо рассказов О'Генри сегодня займусь "Гамлетом".''')
print("""Вместо рассказов О'Генри сегодня займусь "Гамлетом".""")

#""" """ (тройные кавычки) используются для заключения многострочных строковых данных,
# для которых одинарные и двойные кавычки не могут применяться.
print("""Один
Два
Три""")
